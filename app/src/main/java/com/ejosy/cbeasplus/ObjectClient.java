package com.ejosy.cbeasplus;

public class ObjectClient {
     public int id;
    public String phone;
    public String name;
    public String  designation;
    public String avenue;
    public String street;


    public ObjectClient(int id, String phone,  String name, String designation, String avenue, String street)
    {

        this.id = id;
        this.phone = phone;
        this.name = name;
        this.designation = designation;;
        this.avenue = avenue;
        this.street = street;
    }

}
